const mongoose=require("mongoose");


const userSchema=mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true,
        minLength:8
    },
    confirmPassword:{
        type:String,
        required:true,
        minLength:8

    }
    
});
const userModel=mongoose.model("userModel",userSchema);

module.exports=userModel;